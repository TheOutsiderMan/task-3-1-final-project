package by.tr.web.kinorating.dao.exception;

public class DAOException extends Exception {

	private static final long serialVersionUID = -1066773313590369951L;

	public DAOException() {
		super();
	}

	public DAOException(String message, Throwable cause) {
		super(message, cause);
	}

	public DAOException(String message) {
		super(message);
	}

	public DAOException(Throwable cause) {
		super(cause);
	}

}
